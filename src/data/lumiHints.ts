// Функция для получения подсказки по вопросу
export const getHintForQuestion = (
  questionId: string,
  topicKey?: string
): LumiHint | null => {
  // Сначала ищем конкретную подсказку для вопроса
  if (questionHints[questionId]) {
    return questionHints[questionId];
  }

  // Если нет конкретной, возвращаем общую по теме
  if (topicKey && topicHints[topicKey]) {
    return topicHints[topicKey];
  }

  // Fallback на общую подсказку
  return {
    hint: "Давай разберем этот вопрос вместе! 💡 Подумай о том, что ты уже знаешь про это правило. Что кажется логичным?",
    encouragement: "Не переживай! ✨ Даже сложные вопросы становятся понятными, когда разбираешься в деталях.",
  };
};

