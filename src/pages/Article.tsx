            <ul key={`list-${index}`} className="list-disc list-inside mb-4 space-y-2 ml-4">
              {currentList.map((item, i) => (
                <li key={i} className="text-gray-700 dark:text-gray-300">
                  {renderInlineText(item.replace(/^[-*]\s*/, ""))}
                </li>
              ))}
            </ul>
