/**
 * Date Utilities
 * 
 * Динамические даты для использования на сайте.
 * Автоматически обновляются при наступлении нового года.
 */

/**
 * Текущий год (для копирайтов, футеров)
 */
export const currentYear = new Date().getFullYear();

/**
 * Год экзамена (для бейджей и заголовков)
 * 
 * Логика: Если сейчас ноябрь-декабрь, показываем следующий год,
 * чтобы контент выглядел актуальным для готовящихся к экзамену.
 */
export const examYear = (() => {
    const now = new Date();
    const month = now.getMonth(); // 0-11
    const year = now.getFullYear();

    // Если ноябрь (10) или декабрь (11) - показываем следующий год
    return month >= 10 ? year + 1 : year;
})();
